// Компонент для отображения цветных бейджей с типами и причинами ДТП
const TYPE_COLORS = {
  'Наезд на пешехода': '#e74c3c',
  'Наезд на препятствие': '#e67e22',
  'Столкновение': '#c0392b',
  'Опрокидывание': '#8e44ad',
  'Съезд с дороги': '#2980b9',
  'Наезд на велосипедиста': '#d35400',
  'Прочее': '#7f8c8d',
};

const CAUSE_COLORS = {
  'Выезд на полосу встречного движения': '#c0392b',
  'Состояние водителя': '#8e44ad',
  'Неисправность автомобиля': '#f39c12',
  'Нарушение ПДД': '#e67e22',
  'Превышение скорости': '#e74c3c',
  'Плохие дорожные условия': '#2980b9',
  'Прочее': '#7f8c8d',
};

export default function Badge({ value, kind = 'type' }) {
  const map = kind === 'cause' ? CAUSE_COLORS : TYPE_COLORS;
  const bg = map[value] || '#7f8c8d';
  return (
    <span className="badge" style={{ backgroundColor: bg }}>
      {value}
    </span>
  );
}

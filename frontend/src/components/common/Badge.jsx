// Компонент для отображения типа ДТП или причины в виде цветного бейджа
const TYPE_COLORS = {
  'Наезд на пешехода': '#d97c70',
  'Наезд на препятствие': '#d4a070',
  'Столкновение': '#b86a60',
  'Опрокидывание': '#9c84b8',
  'Съезд с дороги': '#7a9bb8',
  'Наезд на велосипедиста': '#c08562',
  'Прочее': '#a8b1b2',
};

const CAUSE_COLORS = {
  'Выезд на полосу встречного движения': '#b86a60',
  'Состояние водителя': '#9c84b8',
  'Неисправность автомобиля': '#d4b86b',
  'Нарушение ПДД': '#d4a070',
  'Превышение скорости': '#d97c70',
  'Плохие дорожные условия': '#7a9bb8',
  'Прочее': '#a8b1b2',
};

export default function Badge({ value, kind = 'type' }) {
  const map = kind === 'cause' ? CAUSE_COLORS : TYPE_COLORS;
  const bg = map[value] || '#a8b1b2';
  return (
    <span className="badge" style={{ backgroundColor: bg }}>
      {value}
    </span>
  );
}
